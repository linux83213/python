# Définition de la fonction Add
def Add(a, b):
    somme = a + b
    print(f"La somme de {a} et {b} est : {somme}")

# Appels de la fonction avec différents paramètres
Add(3, 5)    # Affiche la somme de 3 et 5
Add(10, 20)  # Affiche la somme de 10 et 20
Add(7, 8)    # Affiche la somme de 7 et 8
Add(15, 25)  # Affiche la somme de 15 et 25
