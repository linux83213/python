# Définition de la fonction qui ajoute "melon" à la liste de fruits
def ajouter_melon():
    fruits = ["pomme", "cerise", "orange"]  # Liste initiale des fruits
    fruits.append("melon")  # Ajoute "melon" à la fin de la liste
    print(fruits)  # Affiche la liste mise à jour

# Appel de la fonction
ajouter_melon()
