# Liste donnée
L = [8, 24, 27, 48, 2, 16, 9, 102, 7, 84, 91]

# Récupérer la valeur maximale et minimale
valeur_max = max(L)  # Trouver la valeur maximale de la liste
valeur_min = min(L)  # Trouver la valeur minimale de la liste

# Affichage des résultats
print(f"La valeur maximale de la liste est : {valeur_max}")
print(f"La valeur minimale de la liste est : {valeur_min}")
