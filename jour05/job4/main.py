# Définition de la fonction qui ajoute "mangue" à l'index 2
def ajouter_mangue():
    fruits = ["pomme", "cerise", "orange", "melon"]  # Liste initiale des fruits
    fruits.insert(2, "mangue")  # Insère "mangue" à l'index 2
    print(fruits)  # Affiche la liste mise à jour

# Appel de la fonction
ajouter_mangue()
